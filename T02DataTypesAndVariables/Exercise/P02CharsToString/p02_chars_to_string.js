function charsToString(firstSymbol, secondSymbol, thirdSymbol) {
    console.log(`${firstSymbol}${secondSymbol}${thirdSymbol}`);
}

charsToString('a', 'b', 'c');
console.log("-------------------------");
charsToString('%', '2', 'o');
console.log("-------------------------");
charsToString('1', '5', 'p');